def _score_tokens(tokens) -> Tuple[float, Dict]:
    SKIP_POS = {'PUNCT','ADP','CCONJ','SCONJ','NUM','SYM','X'}
    content  = [t for t in tokens if getattr(t,'pos','') not in SKIP_POS]
    total = 0.0
    pos_words, neg_words = [], []
    neg_window = 0
    amplify    = 1.0

    for tok in content:
        lemma = (tok.lemma if hasattr(tok,'lemma') else tok).lower()
        if lemma in NEGATION_LEMMAS:
            neg_window = 3; continue
        if lemma in AMPLIFIER_LEMMAS:
            amplify = 1.3; continue
        if lemma in DIMINISHER_LEMMAS:
            amplify = 0.5; continue

        w = FULL_LEXICON.get(lemma, 0.0)
        if w == 0.0:
            if neg_window > 0: neg_window -= 1
            amplify = 1.0
            continue

        if neg_window > 0:
            w *= -0.5
            neg_window -= 1
        w *= amplify
        amplify = 1.0
        total += w
        (pos_words if w > 0 else neg_words).append((lemma, round(w, 2)))

    return total, {
        'positive_words': pos_words, 'negative_words': neg_words,
        'positive_count': len(pos_words), 'negative_count': len(neg_words),
    }


def _normalise(score: float, n: int) -> float:
    if n == 0: return 0.0
    return max(-1.0, min(1.0, round(score / max(math.sqrt(n), 1), 3)))


def _label(score: float) -> str:
    if score > 0.15: return 'positive'
    if score < -0.15: return 'negative'
    return 'neutral'


# ── Публичный API ──────────────────────────────────────────────────────────────

def analyse_text(full_text: str, sentences: list) -> Dict:
    all_tokens = [t for s in sentences for t in s.tokens]
    raw, bd    = _score_tokens(all_tokens)
    norm       = _normalise(raw, len(all_tokens))
    lbl        = _label(norm)

    per = []
    for s in sentences:
        s_raw, s_bd = _score_tokens(s.tokens)
        s_norm      = _normalise(s_raw, len(s.tokens))
        s_lbl       = _label(s_norm)
        per.append({
            'id': s.id, 'score': s_norm,
            'label': s_lbl, 'label_ru': LABEL_RU[s_lbl],
            'scores': {'positive': max(0.0,s_norm), 'negative': abs(min(0.0,s_norm)),
                       'neutral': round(1.0-abs(s_norm),3)},
            'positive_words': s_bd['positive_words'][:5],
            'negative_words': s_bd['negative_words'][:5],
        })

    raw_scores = {
        'positive': max(0.0, round(norm,3)),
        'negative': abs(min(0.0, round(norm,3))),
        'neutral':  round(1.0-abs(norm),3),
        'speech': 0.0, 'skip': 0.0,
    }
    return {
        'overall': lbl, 'overall_ru': LABEL_RU[lbl], 'score': norm,
        'positive_count': bd['positive_count'],
        'negative_count': bd['negative_count'],
        'positive_words': bd['positive_words'][:10],
        'negative_words': bd['negative_words'][:10],
        'per_sentence': per,
        'engine': 'rusentillex',
        'raw_scores': raw_scores,
        'lexicon_size': len(FULL_LEXICON),
    }


